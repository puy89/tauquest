class Course:
    def __init__(self, id, name, hebrew_name, hebrew_departue, semester, start_time, end_time, day, place, kind,
                 building, lecturer):
        self.id = id
        self.name = name
        self.hebrew_name = hebrew_name
        self.hebrew_departure = hebrew_departue
        self.departure = hebrew_departue
        self.semester = semester
        self.start_time = start_time
        self.end_time = end_time
        self.day = day
        self.place = place
        self.kind = kind
        self.building = building
        self.lecturer = lecturer
